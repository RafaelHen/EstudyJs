const escola = "Cod3r"

console.log(escola.charAt()) // imprimindo tal letra.

console.log(escola.charCodeAt(3)) // valor dentro da tabela ASCII

console.log(escola.indexOf('s')) // Procura o index dentro deu uma palavra

console.log(escola.substring(1)) // imprime o restante apartir do indice dado.
console.log(escola.substring(2,4))

console.log('Escola '.concat(escola).concat(" !")) // contatenação
console.log(escola.replace('r', 3)) // Substitui uma letra por outra letra, número... etc

console.log('rafael, israel, kayke'.split(',')) // Substitui uma string por um espaço e cria um array
